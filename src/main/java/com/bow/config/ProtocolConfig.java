package com.bow.config;

/**
 * @author vv
 * @since 2016/9/4.
 */
public class ProtocolConfig {

    private String id;
    private String name;
    private int port;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }
}
